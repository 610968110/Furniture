public class NickNameEditText extends android.support.v7.widget.AppCompatEditText {

    public NickNameEditText(Context context) {
        super(context);
        init();
    }

    public NickNameEditText(Context context, AttributeSet attrs) {
        super(context, attrs);
        init();
    }

    public NickNameEditText(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
        init();
    }

    private void init() {
        addTextChangedListener(new TextWatcher() {
            @Override
            public void beforeTextChanged(CharSequence s, int start, int count, int after) {

            }

            @Override
            public void onTextChanged(CharSequence s, int start, int before, int count) {

            }

            @Override
            public void afterTextChanged(Editable s) {
                String string = s.toString();
                if (!TextUtils.isEmpty(string) && string.length() > 8) {
                    setText(string.substring(0, 8));
                    XTools.UiUtil().showToast("不能超过8个字");
                }
            }
        });
    }

    @Override
    protected void onSelectionChanged(int selStart, int selEnd) {
        super.onSelectionChanged(selStart, selEnd);
        //保证光标始终在最后面
        //防止不能多选
        if (selStart == selEnd) {
            setSelection(getText().length());
        }
    }
}
