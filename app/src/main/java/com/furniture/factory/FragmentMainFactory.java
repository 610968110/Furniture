public class FragmentMainFactory extends FragmentFactory {

    @Override
    public <T extends BaseFragment> BaseFragment createFragment(Class<T> clazz) {
        T fragment = null;
        try {
            fragment = (T) Class.forName(clazz.getName()).newInstance();
        } catch (InstantiationException e) {
            e.printStackTrace();
        } catch (IllegalAccessException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        }
        return fragment;
    }
}
